#include <iostream>
using namespace std;

int main()
{
    // Declare a variable 'c' of type char and assign it the character 'A'
    char c = 'A';
    cout << "Character: " << c << endl;

    // Print the ASCII (integer) code of 'c' by converting it to an int type
    cout << "ASCII code: " << int(c) << endl;

    return 0;
}
